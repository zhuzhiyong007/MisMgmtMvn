package net.zx.lims.dom;

public class Extend {
	private Button buttons;
	private Image images;
	private Checkbox checkboxs;
	private Radio radios;
	private Text texts;
	private Select selects;
	
	/**
	 * @return the buttons
	 */
	public Button getButtons() {
		return buttons;
	}
	/**
	 * @param buttons the buttons to set
	 */
	public void setButtons(Button buttons) {
		this.buttons = buttons;
	}
	/**
	 * @return the images
	 */
	public Image getImages() {
		return images;
	}
	/**
	 * @param images the images to set
	 */
	public void setImages(Image images) {
		this.images = images;
	}
	/**
	 * @return the checkboxs
	 */
	public Checkbox getCheckboxs() {
		return checkboxs;
	}
	/**
	 * @param checkboxs the checkboxs to set
	 */
	public void setCheckboxs(Checkbox checkboxs) {
		this.checkboxs = checkboxs;
	}
	/**
	 * @return the radios
	 */
	public Radio getRadios() {
		return radios;
	}
	/**
	 * @param radios the radios to set
	 */
	public void setRadios(Radio radios) {
		this.radios = radios;
	}
	/**
	 * @return the texts
	 */
	public Text getTexts() {
		return texts;
	}
	/**
	 * @param texts the texts to set
	 */
	public void setTexts(Text texts) {
		this.texts = texts;
	}
	/**
	 * @return the selects
	 */
	public Select getSelects() {
		return selects;
	}
	/**
	 * @param selects the selects to set
	 */
	public void setSelects(Select selects) {
		this.selects = selects;
	}

}
